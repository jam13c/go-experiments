type calcStack struct {
	items []int
}

func newCalc() calcStack {
	return calcStack{make([]int, 0)}
}

func (s *calcStack) push(n int) {
	s.items = append(s.items, n)
}

func (s *calcStack) read() (int, int) {
	l := len(s.items)
	n1, n2 := s.items[l-2], s.items[l-1]
	s.items = s.items[0 : l-2]
	return n1, n2
}

func (s *calcStack) result() int {
	if len(s.items) != 1 {
		panic("Calc stack has more than 1 result")
	}
	return s.items[0]
}

type tokenStack struct {
	items []*token
}

func newStack() tokenStack {
	return tokenStack{make([]*token, 0)}
}

func (s *tokenStack) push(t *token) {
	s.items = append(s.items, t)
}

func (s *tokenStack) peek() *token {
	if len(s.items) == 0 {
		return nil
	}
	return s.items[len(s.items)-1]
}

func (s *tokenStack) pop() *token {
	item := s.peek()
	if item == nil {
		return nil
	}
	s.items = s.items[0 : len(s.items)-1]
	return item
}

type tokenQueue struct {
	items []*token
}

func newQueue() tokenQueue {
	return tokenQueue{make([]*token, 0)}
}

func (q *tokenQueue) peek() *token {
	if len(q.items) == 0 {
		return nil
	}
	return q.items[0]
}

func (q *tokenQueue) push(t *token) {
	q.items = append(q.items, t)
}

func (q *tokenQueue) pop() *token {
	item := q.peek()
	if item == nil {
		return nil
	}
	if len(q.items) == 1 {
		q.items = make([]*token, 0)
	} else {
		q.items = q.items[1:]
	}
	return item
}

